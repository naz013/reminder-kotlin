public class CallsItem {
    private String name;
    private String uri;
    private long date;
    private String number;
    private String numberName;
    private int id;
    private int type;

    public CallsItem(String name, String number, String uri, long date, int id, int type) {
        this.name = name;
        this.number = number;
        this.uri = uri;
        this.date = date;
        this.id = id;
        this.type = type;
        this.numberName = number + (name != null ? name : "");
    }

    public String getNumberName() {
        return numberName;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUri() {
        return uri;
    }

    public void setUri(String uri) {
        this.uri = uri;
    }

    public long getDate() {
        return date;
    }

    public void setDate(long date) {
        this.date = date;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
