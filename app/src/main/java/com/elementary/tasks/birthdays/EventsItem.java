public class EventsItem extends AdapterItem {

    private int day, month, year, color;

    EventsItem(int viewType, Object object, int day, int month, int year, int color) {
        super(viewType, object);
        this.day = day;
        this.month = month;
        this.year = year;
        this.color = color;
    }

    public int getDay() {
        return day;
    }

    public void setDay(int day) {
        this.day = day;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public int getYear() {
        return year;
    }

    public void setYear(int year) {
        this.year = year;
    }

    public int getColor() {
        return color;
    }

    public void setColor(int color) {
        this.color = color;
    }

    @Override
    public String toString() {
        return "EvI{" +
                "d=" + day +
                ", m=" + month +
                ", y=" + year +
                '}';
    }
}
